# int() => mengubah menjadi int
x = int('12')
y = int(12.7)

print(x)
print(y)

# pow => nilai pangkat
var_num1 = pow(3,3) #3 pangkat 3
var_num2 = pow(3,-3) #3 pangkat -3

print(var_num1)
print(var_num2)

# str() => mengubah menjadi String
var_str1 = str(12)
var_str2 = str(12.5)

print(type(var_str1))
print(type(var_str2))